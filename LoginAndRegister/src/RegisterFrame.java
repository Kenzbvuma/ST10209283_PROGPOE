    private void jButtonRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonRegisterActionPerformed
        // TODO add your handling code here:
        
        
       
        boolean checkUsername; // Boolean variable
        boolean checkPassword; // Boolean variable
        //( Farrell , 2019)
        String fn;// Variable for Firstname
        fn = firstname_textfield.getText();// Gets Firstname input
        System.out.println(fn);// To check if method is working
        
        String ln;// Variable for Lastname 
        ln = lastname_textfield.getText();
        System.out.println(ln);// To check if method is working
        
        
        String ru; // variable for username
        ru = Rusername_textfield.getText(); // Gets username input
        System.out.println(ru);// To check if the method is working
        
        
        checkUsername = lg.checkUserName(ru); // Compares 
        String ptf;// Variable for password
        ptf = password_textfield.getText(); // Gets password input
        System.out.println(ptf);// To check if the method is working
        
        
        
        String ptf2;// String for confirmation password
        ptf2 = password_textfield2.getText();// Gets confirmation password input
        System.out.println(ptf2);// To check if the method is working
        
        
        checkPassword = lg.checkPasswordComplexity(ptf2); // Compares password in register frame and in JOption interface
        
        lg.registerUser(checkPassword, checkUsername , ru , ptf2 , fn , ln); // instantiates an object with a registerUser method call
        
        
        if (checkPassword && checkUsername){ // Condition for if username and Password are the same in Register frame and JOption interface 
            lg.setFname(fn);
            lg.setSname(ln);
            lg.setUname(ru); 
            lg.setUpass(ptf2);
        }
        
    
        
    }//GEN-LAST:event_jButtonRegisterActionPerformed

